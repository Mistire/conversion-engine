"""
τ²-Bench evaluation harness wrapper.

Wraps the tau2-bench CLI runner to:
- Write traces to Langfuse after each run
- Update score_log.json with pass@1 + 95% CI
- Tag each run with model, trial number, slice (dev/held_out)
"""
import json
import math
import subprocess
import sys
import uuid
from datetime import datetime
from pathlib import Path
from typing import Optional

TAU2_DIR = Path(__file__).parent.parent.parent / "tau2-bench"
EVAL_DIR = Path(__file__).parent
SCORE_LOG = EVAL_DIR / "score_log.json"
TRACE_LOG = EVAL_DIR / "trace_log.jsonl"

# Dev slice: tasks 0-29 (30 tasks). Held-out: 30-49 (20 tasks).
# Held-out partition is generated by program staff — do not expose task content.
DEV_SLICE = list(range(30))
HELD_OUT_SLICE = list(range(30, 50))


# ─── Statistical helpers ──────────────────────────────────────────────────────

def pass_at_k_ci(successes: int, trials: int, z: float = 1.96) -> tuple[float, float, float]:
    """
    Returns (mean, lower_95ci, upper_95ci) for pass@1.
    Uses Wilson score interval for small samples.
    """
    if trials == 0:
        return 0.0, 0.0, 0.0
    p = successes / trials
    denom = 1 + z**2 / trials
    centre = (p + z**2 / (2 * trials)) / denom
    margin = (z * math.sqrt(p * (1 - p) / trials + z**2 / (4 * trials**2))) / denom
    return round(p, 4), round(max(0, centre - margin), 4), round(min(1, centre + margin), 4)


# ─── Score log ────────────────────────────────────────────────────────────────

def load_score_log() -> list[dict]:
    if SCORE_LOG.exists():
        with open(SCORE_LOG) as f:
            return json.load(f)
    return []


def save_score_log(entries: list[dict]) -> None:
    SCORE_LOG.parent.mkdir(exist_ok=True)
    with open(SCORE_LOG, "w") as f:
        json.dump(entries, f, indent=2)


def append_score_entry(entry: dict) -> None:
    entries = load_score_log()
    entries.append(entry)
    save_score_log(entries)


# ─── Trace log ────────────────────────────────────────────────────────────────

def append_trace(trace: dict) -> None:
    TRACE_LOG.parent.mkdir(exist_ok=True)
    with open(TRACE_LOG, "a") as f:
        f.write(json.dumps(trace) + "\n")


# ─── Langfuse logger ──────────────────────────────────────────────────────────

def log_to_langfuse(run_id: str, results: list[dict], model: str, slice_name: str) -> None:
    try:
        import os
        from langfuse import Langfuse
        lf = Langfuse(
            public_key=os.getenv("LANGFUSE_PUBLIC_KEY", ""),
            secret_key=os.getenv("LANGFUSE_SECRET_KEY", ""),
            host=os.getenv("LANGFUSE_HOST", "https://cloud.langfuse.com"),
        )
        for r in results:
            trace = lf.trace(
                id=r.get("trace_id", str(uuid.uuid4())),
                name=f"tau2_retail_{slice_name}",
                metadata={
                    "run_id": run_id,
                    "model": model,
                    "task_id": r.get("task_id"),
                    "passed": r.get("passed"),
                    "slice": slice_name,
                },
            )
        lf.flush()
    except Exception as e:
        print(f"[harness] Langfuse logging skipped: {e}", file=sys.stderr)


# ─── Single trial runner ──────────────────────────────────────────────────────

def run_tau2_trial(
    model: str,
    domain: str = "retail",
    num_tasks: int = 5,
    task_ids: Optional[list[int]] = None,
    trial_index: int = 0,
) -> list[dict]:
    """
    Run one trial of tau2-bench on the given domain.
    Returns list of per-task result dicts with trace_id, passed, task_id.
    """
    tau2_bin = TAU2_DIR / ".venv" / "bin" / "tau2"
    save_to = EVAL_DIR / "tau2_runs" / f"trial_{trial_index}"
    save_to.mkdir(parents=True, exist_ok=True)

    cmd = [
        str(tau2_bin),
        "run",
        "--domain", domain,
        "--agent-llm", model,
        "--user-llm", model,
        "--num-trials", "1",
        "--num-tasks", str(num_tasks),
        "--save-to", str(save_to),
        "--auto-resume",
    ]

    # task-ids are space-separated positional args after the flag
    if task_ids:
        cmd += ["--task-ids"] + [str(t) for t in task_ids]

    try:
        proc = subprocess.run(
            cmd,
            cwd=str(TAU2_DIR),
            capture_output=False,   # let output stream to terminal
            text=True,
            timeout=1800,
        )
    except subprocess.TimeoutExpired:
        print("[harness] trial timed out", file=sys.stderr)
        return []
    except Exception as e:
        print(f"[harness] tau2 run failed: {e}", file=sys.stderr)
        return []

    # tau2 writes a single results.json inside the save-to directory
    results_file = save_to / "results.json"
    results = []
    if not results_file.exists():
        print(f"[harness] results.json not found at {results_file}", file=sys.stderr)
        return []

    try:
        with open(results_file) as f:
            data = json.load(f)
    except Exception as e:
        print(f"[harness] failed to parse results.json: {e}", file=sys.stderr)
        return []

    for sim in data.get("simulations", []):
        task_id = str(sim.get("task_id", "unknown"))
        reward = sim.get("reward_info", {}).get("reward", 0)
        passed = reward >= 1.0
        trace_id = sim.get("id", str(uuid.uuid4()))

        results.append({
            "task_id": task_id,
            "passed": passed,
            "reward": reward,
            "trace_id": trace_id,
            "trial": trial_index,
            "raw": sim,
        })
        append_trace({
            "trace_id": trace_id,
            "task_id": task_id,
            "trial": trial_index,
            "passed": passed,
            "reward": reward,
            "timestamp": datetime.utcnow().isoformat(),
            "model": model,
            "domain": domain,
            "duration_s": sim.get("duration"),
            "termination_reason": sim.get("termination_reason"),
        })

    return results


# ─── Multi-trial baseline ─────────────────────────────────────────────────────

def run_baseline(
    model: str,
    domain: str = "retail",
    num_trials: int = 5,
    slice_name: str = "dev",
    num_tasks: int = 30,
    task_ids: Optional[list[int]] = None,
) -> dict:
    """
    Run N trials and compute pass@1 with 95% CI.
    Appends result to score_log.json.
    Returns the score entry dict.
    """
    run_id = str(uuid.uuid4())
    t_start = datetime.utcnow()

    all_results = []
    for trial_i in range(num_trials):
        print(f"[harness] Trial {trial_i + 1}/{num_trials}...")
        results = run_tau2_trial(
            model=model,
            domain=domain,
            num_tasks=num_tasks,
            task_ids=task_ids,
            trial_index=trial_i,
        )
        all_results.extend(results)

    t_end = datetime.utcnow()
    wall_clock_s = (t_end - t_start).total_seconds()

    # Aggregate pass@1 across all trials
    total = len(all_results)
    successes = sum(1 for r in all_results if r["passed"])
    mean, ci_lo, ci_hi = pass_at_k_ci(successes, total)

    # Per-task pass rate
    task_results: dict[str, list[bool]] = {}
    for r in all_results:
        task_results.setdefault(str(r["task_id"]), []).append(r["passed"])
    per_task_pass = {t: sum(v) / len(v) for t, v in task_results.items()}

    score_entry = {
        "run_id": run_id,
        "model": model,
        "domain": domain,
        "slice": slice_name,
        "num_trials": num_trials,
        "num_tasks": len(task_results),
        "total_task_trials": total,
        "successes": successes,
        "pass_at_1_mean": mean,
        "pass_at_1_ci_lo": ci_lo,
        "pass_at_1_ci_hi": ci_hi,
        "wall_clock_s": round(wall_clock_s, 1),
        "timestamp": t_start.isoformat(),
        "per_task_pass_rate": per_task_pass,
    }

    append_score_entry(score_entry)
    log_to_langfuse(run_id, all_results, model, slice_name)

    print(f"\n[harness] Results for {model} on {domain} ({slice_name} slice):")
    print(f"  pass@1 = {mean:.3f}  [{ci_lo:.3f}, {ci_hi:.3f}] 95% CI")
    print(f"  {successes}/{total} tasks passed across {num_trials} trials")
    print(f"  wall clock: {wall_clock_s:.0f}s")

    return score_entry
